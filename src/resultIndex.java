    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String roll = rollnumber.getText();
        String Physics = physics.getText();
        String Maths = maths.getText();
        String EM = em.getText();
        String DBMS = dbms.getText();
        String OS = os.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "Kishan@123");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select roll_no from student where roll_no = '"+roll+"'");
            if(rs.next()){
                st.executeUpdate("insert into Result(roll_no, physics, maths, EM, DBMS, OS) values('"+roll+"', '"+Physics+"', '"+Maths+"', '"+EM+"', '"+DBMS+"', '"+OS+"')");
                JOptionPane.showMessageDialog(null, "Marks Save Successfully");
                setVisible(false);
                new resultIndex().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "This roll number is not registered!");

            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_saveActionPerformed

